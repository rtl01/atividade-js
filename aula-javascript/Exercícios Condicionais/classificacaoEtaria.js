const idade = 35;

if(idade >= 0 && idade <= 11){
    console.log('Você é criança!');
}else if(idade >= 12 && idade <= 17){
    console.log('Você é adolescente!');
}else if(idade >= 18 && idade <= 43){
    console.log('Você é adulto!');
}else if(idade >= 44){
    console.log('Você é idoso!');
}